const I18nContext = createContext({
    lang: DEFAULT_LANGUAGE,
    messages: MESSAGES[DEFAULT_LANGUAGE]
});

export const getMessages = (lang = DEFAULT_LANGUAGE) => {
    const normalized = normalizeLanguage(lang);
    return MESSAGES[normalized] || MESSAGES[DEFAULT_LANGUAGE];
};

export const getSectionMessages = (section, lang = DEFAULT_LANGUAGE) => {
    const messages = getMessages(lang);
    return messages?.[section] || MESSAGES[DEFAULT_LANGUAGE]?.[section] || {};
};

export const getCurrentSectionMessages = (section) => getSectionMessages(section, getDocumentLanguage());

export const useDocumentLanguage = () => {
    const [lang, setLang] = useState(() => getDocumentLanguage());

    useEffect(() => {
        if (typeof window === 'undefined') return undefined;

        const handleLanguageChange = (event) => {
            const nextLang = event?.detail?.lang || getDocumentLanguage();
            setLang(normalizeLanguage(nextLang));
        };

        window.addEventListener(LANGUAGE_CHANGE_EVENT, handleLanguageChange);
        return () => window.removeEventListener(LANGUAGE_CHANGE_EVENT, handleLanguageChange);
    }, []);

    return lang;
};

export const useDocumentSectionMessages = (section) => {
    const lang = useDocumentLanguage();
    return getSectionMessages(section, lang);
};

